class Solution {

    // Note that the size of the array is n-1
    int missingNumber(int n, int arr[]) {
        int count = 0;
        int count2 = 0;
        for(int i=1; i<=n; i++){
            count += i;
        }
        for(int j=0; j<arr.length; j++){
            count2 += arr[j];
        }
        return Math.abs(count2 - count);

        
    }
}